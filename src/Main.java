public class Main {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) { // for문 5번 -> 행 5줄 찍겠다는 뜻
            for (int j = 0; j < 5; j++) {
                System.out.println("i = " + i);
                System.out.println("j = " + j);
            }
        }

    }
}
